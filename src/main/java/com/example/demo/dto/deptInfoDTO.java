package com.example.demo.dto;

public class deptInfoDTO {
    private String dept;
    private int years;
    private int common;
    private int must_major;
    private int sel_major;
    private int normal;
    private int basic;
    private int english;

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public int getYears() {
        return years;
    }

    public void setYears(int years) {
        this.years = years;
    }

    public int getCommon() {
        return common;
    }

    public void setCommon(int common) {
        this.common = common;
    }

    public int getMust_major() {
        return must_major;
    }

    public void setMust_major(int must_major) {
        this.must_major = must_major;
    }

    public int getSel_major() {
        return sel_major;
    }

    public void setSel_major(int sel_major) {
        this.sel_major = sel_major;
    }

    public int getNormal() {
        return normal;
    }

    public void setNormal(int normal) {
        this.normal = normal;
    }

    public int getBasic() {
        return basic;
    }

    public void setBasic(int basic) {
        this.basic = basic;
    }

    public int getEnglish() {
        return english;
    }

    public void setEnglish(int english) {
        this.english = english;
    }
}
